SELECT
	r.DESCRIPCION AS OTRAS_COMPLICACIONES,
	(COUNT(*) / (SELECT COUNT(*) FROM covid.COVID19MEXICO2021 cm WHERE cm.FECHA_DEF != '9999-99-99')) * 100 AS PORCENTAJE_DEF
FROM 
	covid.COVID19MEXICO2021 cm
INNER JOIN 
	covid.Respuestas r ON r.CLAVE = cm.OTRA_COM  
WHERE 
	FECHA_DEF != '9999-99-99'
GROUP BY
	r.DESCRIPCION
ORDER BY
	r.DESCRIPCION DESC